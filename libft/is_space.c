#include "libft.h"

int	is_space(char *line)
{
	int	i;

	i = 0;
	while (line[i] == ' ' || line[i] == '\t'
		|| line[i] == '\n')
		i++;
	if (line[i] == '\0')
		return (1);
	return (0);
}
