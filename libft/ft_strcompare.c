#include "libft.h"

int	str_compare(char *s_1, char *s_2, int max)
{
	int	a;

	a = 0;
	while (a < max && *s_1 && *s_2 && s_1[a] == s_2[a])
		a++;
	if (sizeof_str(s_1, '\0') == a && a == max)
		return (1);
	return (0);
}
